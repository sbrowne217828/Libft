#include "libft.h"

const char	*ft_endofnum(const char *str)
{
	const char	*s;

	while (*str == '0')
		(str)++;
	s = str;
	while (*s)
	{
		if (!(*s >= '0' && *s <= '9'))
			break ;
		s++;
	}
	return (s);
}
